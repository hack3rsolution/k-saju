// ── Utility Functions ─────────────────────────────────────────────────────────

export function densityToCount(density: KCultureDensity): number {
  switch (density) {
    case 'high':   return 3;
    case 'medium': return 2;
    case 'low':    return 1;
    case 'none':   return 0;
  }
}

export function adjustDensityByFeedback(
  base: KCultureDensity,
  positives: number,
  negatives: number,
): KCultureDensity {
  const total = positives + negatives;
  if (total === 0) return base;
  const idx = DENSITY_ORDER.indexOf(base);
  if (positives / total > 0.6 && idx < DENSITY_ORDER.length - 1) return DENSITY_ORDER[idx + 1];
  if (negatives / total > 0.6 && idx > 0)                         return DENSITY_ORDER[idx - 1];
  return base;
}

export function pickKCultureRefs(category: FortuneCategory, count: number): string[] {
  if (count <= 0) return [];
  const pool = [...KCULTURE_REFERENCES[category]];
  const result: string[] = [];
  for (let i = 0; i < Math.min(count, pool.length); i++) {
    const randIdx = Math.floor(Math.random() * (pool.length - i));
    result.push(pool[randIdx]);
    [pool[randIdx], pool[pool.length - 1 - i]] = [pool[pool.length - 1 - i], pool[randIdx]];
  }
  return result;
}

export function buildKCultureLayer(
  frame: CulturalFrame,
  category: FortuneCategory,
  positives: number,
  negatives: number,
): string {
  const baseDensity = KCULTURE_DENSITY[frame];
  const density    = adjustDensityByFeedback(baseDensity, positives, negatives);
  const count      = densityToCount(density);
  if (count === 0) return '';

  const refs = pickKCultureRefs(category, count);
  if (refs.length === 0) return '';

  return (
    `\nK-CULTURE FLAVOR — naturally weave 1-2 of these into the reading ` +
    `(do NOT name any real celebrities, specific people, or actual brands):\n` +
    refs.map((r) => `- ${r}`).join('\n')
  );
}

